package domain.antiprocrastination

/**
 * Собирает baseline и optimized prompt для одного и того же кейса.
 *
 * Baseline специально простой и рыхлый: так проще увидеть,
 * что именно даёт оптимизация prompt-шаблона и параметров модели.
 */
class AntiProcrastinatorPromptBuilder {

    fun buildBaselinePrompt(
        request: AntiProcrastinatorRequest,
    ): String {
        return """
            Помоги человеку перестать прокрастинировать и начать задачу.

            Задача:
            ${request.task}

            Что мешает:
            ${request.blocker}

            Время:
            ${request.availableMinutes} минут

            Уровень энергии:
            ${request.energyLevel}
        """.trimIndent()
    }

    fun buildOptimizedPrompt(
        request: AntiProcrastinatorRequest,
    ): String {
        return """
            Ты — сервис Anti-Procrastinator.
    
            Твоя задача — помочь человеку начать задачу в ближайшие ${request.availableMinutes} минут.
            Ответ должен быть коротким, практичным и без мотивационной воды.

            Вход:
            Задача: ${request.task}
            Что мешает: ${request.blocker}
            Доступное время: ${request.availableMinutes} минут
            Уровень энергии: ${request.energyLevel}
            Предыдущий контекст: ${request.previousContext.ifBlank { "нет" }}
            
            Как выбирать действия:
            - Первое действие должно быть маленьким и физически выполнимым за 2–5 минут.
            - Действие должно оставлять видимый результат: строка текста, открытый файл, вымытая вещь, выбранный материал, отправленное сообщение, убранный предмет.
            - Не заменяй выполнение подготовкой, если можно сделать маленький кусок самой задачи.
            - Не предлагай полный план всей большой задачи.
            - Не предлагай план на завтра.
            - Не предлагай календарь, медитацию, дыхание, анализ личности или мотивационные рассуждения.
            - Если энергии мало, уменьши первый шаг до минимального физического действия.
            - Весь план должен помещаться в доступное время.
            - Не копируй формулировки из инструкции как содержимое ответа.

            Ответь строго в таком формате:
    
            Сейчас:
            коротко опиши минимальный подход
    
            Первое действие:
            одно конкретное действие на 2–5 минут
    
            Пошаговый план, где описываются следующие маленькие шаги.
    
            Не трогать:
            - что сейчас не нужно расширять или улучшать
            - что сейчас не нужно начинать параллельно
    
            Минимум:
            какой видимый результат достаточен, чтобы остановиться
        """.trimIndent()
    }
}