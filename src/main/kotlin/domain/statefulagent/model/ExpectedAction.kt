package domain.statefulagent.model

/**
 * Какого действия оркестратор ждёт после текущего ответа.
 *
 * Используется, чтобы отличать обычный пользовательский ввод
 * от подтверждения плана и автоматического продолжения.
 */
enum class ExpectedAction {
    /** Ждём обычное сообщение пользователя. */
    USER_MESSAGE,

    /** Ждём явное подтверждение или правки плана. */
    APPROVE_PLAN,

    /** Оркестратор может сам продолжить следующий stage. */
    AUTO_CONTINUE,

    /** Никаких действий больше не нужно. */
    NONE,
}